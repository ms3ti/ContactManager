                    while (pubSub == null && !tokenSource.Token.IsCancellationRequested) {
                        await Task.Delay(DelayTimeSpan).ConfigureAwait(false);
                    }

                    if (tokenSource.Token.IsCancellationRequested) {
                        return;
                    }

                    if (!pubSub.IsAvailable(out var exception)) {
                        connectionFails = await ProcessJobFailure(connectionFails, ConnectionRetriesTreshold, EventLogErrorMessageConnections, exception).ConfigureAwait(false);
                        AddJob(jobAction, key, dependencyKey);
                        return;
                    }

                    try {
                        connectionFails = 0;
                        jobAction(); // Light a candle and pray for action to end
                        jobFails = 0;
                        lock (lockObject) {
                            if (!key.IsNullOrEmpty() && dependencies.TryGetValue(key, out var kp)) {
                                kp.Apply((pair) => AddJob(pair.First, pair.Second));
                                dependencies.Remove(key);
                            }
                        }
                    } catch (Exception ex) {
                        if (pubSub.IsAvailable()) {
                            jobFails = await ProcessJobFailure(jobFails, JobRetriesTreshold, EventLogErrorMessageJobs, ex).ConfigureAwait(false);
                        } else {
                            jobFails = 0;
                            connectionFails = await ProcessJobFailure(connectionFails, ConnectionRetriesTreshold, EventLogErrorMessageConnections, ex).ConfigureAwait(false);
                        }
                        AddJob(jobAction, key, dependencyKey);
                    }
                }, tokenSource.Token).Unwrap();
                // Before even thinking about removing the Unwrap above
                // Read this https://stackoverflow.com/questions/58855484/async-in-task-continuewith-behavior
                // Then think again.
            }
        }

