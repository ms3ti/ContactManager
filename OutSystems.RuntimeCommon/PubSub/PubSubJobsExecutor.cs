        public void AddJob(Action jobAction, string key = null, string dependencyKey = null) {
            if (tokenSource.Token.IsCancellationRequested || isOnCleanupState) {
                return; // Discard jobs if disposing
            }

            lock (lockObject) {
                if (!key.IsNullOrEmpty() && !dependencies.ContainsKey(key)) {
                    dependencies[key] = new Queue<Pair<Action, string>>();
                }

                if (!dependencyKey.IsNullOrEmpty() && dependencies.TryGetValue(dependencyKey, out var queue)) {
                    queue.Enqueue(new Pair<Action, string>(jobAction, key));
                    return; // Need to execute the dependencies first!
                }

