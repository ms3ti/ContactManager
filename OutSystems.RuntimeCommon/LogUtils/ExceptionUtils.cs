using System;
using System.Linq;

namespace OutSystems.RuntimeCommon.Utils {

    public static class ExceptionUtils {
        public static string GetExceptionStack(this Exception ex) {
            string msg = string.Empty;
            int count = 1;
            while (ex != null) {
                if (count != 1) {
                    msg += Environment.NewLine;
                }
                string target = "NULL";
                try {
                    if (ex.TargetSite != null) {
                        target = ex.TargetSite.DeclaringType.FullName + "." + ex.TargetSite.Name;
                    }
                } catch (Exception) {
                    // When using remoting we can get FileNotFoundException for assemblies not referenced
                }
                var n = Environment.NewLine;
                msg = $"[{count}] {ex.GetType().FullName}{n}Target : {target}{n}Source : {ex.Source}{n}Message : {ex.Message}{n}Stack Trace{n}{ex.StackTrace}";
                ex = ex.InnerException;
                count++;
            }
            return msg;
        }

        public static string GetFlattenedErrorMessage(this Exception e) {
            var ae = e as AggregateException;
            if (ae != null) {
                return string.Join(Environment.NewLine, ae.Flatten().InnerExceptions.Select(ie => ie.Message));
            }
            return e.Message;
        }
    }
}
