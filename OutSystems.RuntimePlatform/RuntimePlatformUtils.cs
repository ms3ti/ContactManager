        /// <summary>
        /// Convert an string to object.
        /// To be used for retrieve values from a database string field
        /// </summary>
        /// <param name="val">String to be converted</param>
        /// <param name="datatype">The runtime string representation of the desired datatype.</param>
        /// <param name="isSecret">Indicates if the value needs to be hidden</param>
        /// <returns>An object created from the given String based on the data type.</returns>
        internal static object ConvertFromString(string val, string datatype, bool isSecret) {
            // This method is also present in DatabaseOperations.Infrastructure\DBEntityRecords.cs
            // TODO: rbg-pitstop: this could be refactored into some common place.
            switch (datatype) {
                case "rtText":
                case "rtPhoneNumber":
                case "rtEmail":
                    return val;

                case "rtInteger":
                    if (val == string.Empty) {
                        val = "0";
                    } 
                    if (ScriptableBuiltInFunction.TextToIntegerValidate(val)) {
                        return ScriptableBuiltInFunction.TextToInteger(val);
                    } else { // Try implicit conversion
                        LogIncompatibleDataTypeWarning(val, datatype, isSecret);
                        return Convert.ToInt32(Math.Truncate(ImplicitConvertToNumber(val)));
                    }
                case "rtLongInteger":
                    if (val == string.Empty) {
                        val = "0";
                    }
                    if (ScriptableBuiltInFunction.TextToLongIntegerValidate(val)) {
                        return ScriptableBuiltInFunction.TextToLongInteger(val);
                    } else { // Try implicit conversion
                        LogIncompatibleDataTypeWarning(val, datatype, isSecret);
                        return Convert.ToInt64(Math.Truncate(ImplicitConvertToNumber(val)));
                    }
                case "rtDecimal":
                case "rtCurrency":
                    if (val == string.Empty) {
                        val = "0.0";
                    }
                    if (ScriptableBuiltInFunction.TextToDecimalValidate(val)) {
                        return ScriptableBuiltInFunction.TextToDecimal(val);
                    } else { // Try implicit conversion
                        LogIncompatibleDataTypeWarning(val, datatype, isSecret);
                        return ImplicitConvertToNumber(val);
                    }
                case "rtBoolean":
                    // R11BRT-763 (why? :sadface:)
                    // Since Platform version 9 this function return true when converting from an empty string 
                    // However Encrypted(/Secret) Site Properties don't have a default value which means the current
                    // hammer that we have on the platform to force the default value of regular site properties to false
                    // doesn't work for the Encrypted(/Secret) Site Properties
                    // as such this if condition was added
                    if (isSecret && val == string.Empty) {
                        val = "false";
                    } else if (val == string.Empty) {
                        val = "true";
                    }
                    return val?.ToLowerInvariant() == "true";

                case "rtDateTime":
                    if (string.IsNullOrEmpty(val)) {
                        val = "1900-01-01 00:00:00";
                    }
                    return ScriptableBuiltInFunction.TextToDateTime(val);

                case "rtDate":
                    if (string.IsNullOrEmpty(val)) {
                        val = "1900-01-01";
                    }
                    return ScriptableBuiltInFunction.TextToDate(val);

                case "rtTime":
                    if (string.IsNullOrEmpty(val)) {
                        val = "00:00:00";
                    }
                    return ScriptableBuiltInFunction.TextToTime(val);

                default:
                    throw (new ArgumentException("Don't know how to convert " + datatype));
            }
        }

