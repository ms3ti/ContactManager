namespace OutSystems.HubEdition.RuntimePlatform.Internal.TelemetryProvider {
    public static class TelemetryDBProviderCache {

        private static volatile ConcurrentDictionary<string, bool> openConnections = new ConcurrentDictionary<string, bool>();

        /// <summary>
        /// It adds the application and connection into a ConcurrentDictionary to make sure
        /// we don't send the same telemetry many times.
        /// It returns true if the application-connection was added, false if not.
        /// </summary>
        /// <param name="applicationName">Name of the application currently executed</param>
        /// <param name="connectionName">Name of the database connection being cached</param>
        /// <returns>Returns true if the application-connection was added to the cache, false if it already exists</returns>
        public static bool SetCache(string applicationName, string connectionName) {

            var cacheKey = applicationName + "-" + connectionName;

            return openConnections.TryAdd(cacheKey, true);

        }
