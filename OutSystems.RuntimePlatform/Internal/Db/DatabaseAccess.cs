        public static T ExecuteWithoutRequestTimeout<T>(Func<T> action) {
#if !NETSTANDARD
            if (System.Web.HttpContext.Current == null) {
                return action();
            }

            if (!RuntimeSettingsProvider.Instance.Get(RuntimePlatformSettings.Request.UseIISRequestCancelation)) {
                return action(); // The new request timeout mechanism doesn't need this workaround
            }

            int oldScriptTimeout = System.Web.HttpContext.Current.Server.ScriptTimeout;
            try {
                // ScriptTimeout requires a positive number so we need to set to int.MaxValue to "disable" request timeouts
                // https://forums.asp.net/t/1925766.aspx?Httpruntime+ExecutionTimeOut+
                // https://referencesource.microsoft.com/#system.web/httpserverutility.cs,f8f47afb04ad1133,references
                System.Web.HttpContext.Current.Server.ScriptTimeout = int.MaxValue;
                return action();
            } finally {
                System.Web.HttpContext.Current.Server.ScriptTimeout = oldScriptTimeout;
            }
#else
            return action();
#endif
        }

        // Used at runtime to check if we need to force the code to execute the query as having multiplicity Multiple
        public static bool CheckForceMultiplicityMultiple(Transaction transaction) {
            if (transaction.DatabaseServices.DatabaseConfiguration.DatabaseProvider.Key.Serialize() == "SqlServer") {
                return transaction.DriverTransaction.IsolationLevel != IsolationLevel.ReadUncommitted;
            }
            return false;
        }
