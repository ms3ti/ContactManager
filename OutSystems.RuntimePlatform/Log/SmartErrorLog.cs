    public class SmartErrorLog {

        private DateTime lastLogTime;
        private int countLogs;
        private bool waitUntilLogAgain;

        private SmartErrorLog() {
            countLogs = 0;
            lastLogTime = DateTime.Now;
            waitUntilLogAgain = false;
        }

        private void Log(Exception exception, string stackTrace, HeContext heContext, string key, int retries) {
            bool shouldLog = true;

            if (countLogs >= 1000) {
                waitUntilLogAgain = true;
                countLogs = 0;
            }
            if (waitUntilLogAgain) {
                TimeSpan difftime = DateTime.Now.Subtract(lastLogTime);
                shouldLog = difftime.TotalHours >= 1;
            }

            if (shouldLog) {
                waitUntilLogAgain = false;
                string message = "Error getting resource " + key +
                                 "\r\nretry: " + (5 - retries) +
                                 "\r\n" + exception.Message;
                ErrorLog.LogApplicationError(message, stackTrace, heContext, "Global");
                lastLogTime = DateTime.Now;
                countLogs++;
            }
        }


        private static SmartErrorLog singleton = null;

        public static void LogError(Exception e, string stackTrace, HeContext heContext, string key, int retries) {
            if (singleton == null) {
                singleton = new SmartErrorLog();
            }
            singleton.Log(e, stackTrace, heContext, key, retries);
        }
    }
