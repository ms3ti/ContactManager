        public void Validate() {

            if (!ignoreCSRFToken && csrfToken == null) { // Unacceptable
                AddMessage("csrfToken not found in request headers");
                Anonymize();      
                throw new InvalidLoginException("Invalid CSRF request");
            }

            if (httpCookie == null || userCookie == null) {
                AddMessage($"{cookieNames.HttpOnlyLoginCookieName} or {cookieNames.UserAccessibleLoginCookieName} could not be parsed");
                Anonymize();
            }

            if (!ValidSignature()) {
                // Downgrade to anonymous cookies.
                // You might be wondering why we let the guy go as anonymous
                // we do this so that when we change the cookie format
                // users can upgrade their login cookies by logging in again.
                AddMessage($"{cookieNames.HttpOnlyLoginCookieName} signature (HMAC) invalid");
                Anonymize();
            }

            if (!ignoreCSRFToken && !csrfToken.SlowEquals(userCookie.CSRFToken)) { // Unaceptable
                AddMessage($"csrfToken header differs from {cookieNames.UserAccessibleLoginCookieName} crf token");
                Anonymize();
                throw new InvalidLoginException("Invalid CSRF request");
            }

            if (!IsAnonymous) {
                mobileLoginInfo = MobileLoginReadWriter.Read(configuration.EncryptKey, httpCookie.LoginId);
            } else {
                mobileLoginInfo = new MobileLoginInfo {
                    TenantId = configuration.DefaultTenantId
                };
            }

            if (userCookie.UserId != mobileLoginInfo.UserId) {
                AddMessage($"{cookieNames.HttpOnlyLoginCookieName} lid inconsistent");
                throw new InvalidLoginException("Inconsistent Login Info");
            }

        }

        public void Refresh() {
            // If IsAnonymous there is nothing to do
            if (IsAnonymous) {
                return;
            }

            var currentDate = DateTime.UtcNow;
            long currentDateInSeconds = (long)TimeSpan.FromTicks(currentDate.Ticks).TotalSeconds;

            // Checks if is within the time where we trust in the information stored in the cookie
            if (configuration.LoginCacheTime <= TimeSpan.Zero || httpCookie.NeedsToBeRefreshedAt(currentDateInSeconds)) {
                AddMessage($"Cache Time reached, refreshing {cookieNames.HttpOnlyLoginCookieName}");
                // If not, get information from database
                using (var trans = DatabaseAccess.ForSystemDatabase.GetRequestTransaction()) {
                    using (var reader = DBRuntimePlatform.Instance.MobileLoginGet(trans, LoginId)) {
                        if (!reader.Read()) { // If the session was deleted from the database due to be expired
                            AddMessage($"Session deleted from DB due to be expired", $"for login id '{LoginId.FormatSensitiveString()}'");
                            Anonymize();
                            return;
                        }
                        mobileLoginInfo.UserId = reader.SafeGet<int>("USER_ID");
                        mobileLoginInfo.TenantId = reader.SafeGet<int>("TENANT_ID");
                        httpCookie.ExpirationDate = reader.SafeGet<long>("EXPIRATION_DATE");
                        mobileLoginInfo.IsPersistent = reader.SafeGet<bool>("IS_PERSISTENT");

                        userCookie.UserId = mobileLoginInfo.UserId;
                    }

                    // Since we already fetched the information from the database, update the TimeUntilUpdate
                    httpCookie.ExtendTimeUntilUpdateStartingFrom(currentDateInSeconds, configuration.LoginCacheTime);
                    AddMessage($"{cookieNames.HttpOnlyLoginCookieName} next refresh (tuu): {currentDate + configuration.LoginCacheTime}");
                }
            } else {
                // Use the encrypted information in the cookie's LoginId
                AddMessage($"Cache Time not reached yet, trusting received {cookieNames.HttpOnlyLoginCookieName}");
                userCookie.UserId = mobileLoginInfo.UserId;
                return;
            }

            // If cookie is within the expiration date, check if the login is still valid
            if (httpCookie.IsValidAt(currentDateInSeconds)) { 
                using (var trans = DatabaseAccess.ForSystemDatabase.GetCommitableTransaction()) {
                    // If login has not expired, extend expiration time
                    if (DBRuntimePlatform.Instance.MobileLoginIsValidAt(trans, LoginId, currentDateInSeconds)) {
                        var extensionTime = mobileLoginInfo.IsPersistent ? configuration.PersistentLoginMaxIdleTime : configuration.SessionLoginMaxIdleTime;
                        httpCookie.ExtendExpirationDateFrom(currentDateInSeconds, extensionTime);
                        DBRuntimePlatform.Instance.MobileLoginExtendExpirationDateUntil(trans, LoginId, httpCookie.ExpirationDate);
                        dirty = true;
                        AddMessage(
                            $"Session extended to {currentDate + extensionTime} in DB and {cookieNames.HttpOnlyLoginCookieName} 'exp'", 
                            $"for user id '{mobileLoginInfo.UserId}'"
                        );
                    } else { // Login isn't valid, downgrade to anonymous
                        AddMessage($"Session has expired on the DB", $"for user id '{mobileLoginInfo.UserId}'");
                        Anonymize();
                    }
                }
            } else { // Login is already expired, log out
                AddMessage($"{cookieNames.HttpOnlyLoginCookieName} 'exp' expired", $"for user id '{mobileLoginInfo.UserId}'");
                Anonymize();
            }
        }

