    [System.Reflection.Obfuscation(Exclude = true, Feature = "renaming", ApplyToMembers = true)] // used internally RESTService libraries, but no need to obfuscate
    internal class CookieValueReader {

        private string p;
        private int currentIndex;
        private bool bad;

        public CookieValueReader(string payload) {
#if !NETSTANDARD
            p = HttpUtility.UrlDecode(payload.Trim('"'), Encoding.UTF8);
#else
            // Net Core already does this for us RRCT-2717
            p = payload;
#endif
            currentIndex = 0;
            bad = false;
        }

        private bool BadAndFail() {
            bad = true;
            return false;
        }

        private bool Consume(string str) {

            if (currentIndex >= p.Length) {
                return BadAndFail();
            }

            if (currentIndex + str.Length > p.Length) {
                return BadAndFail();
            }

            int strIndex = p.IndexOf(str, currentIndex, str.Length);
            if (strIndex == -1) {
                return BadAndFail();
            }

            currentIndex += str.Length;
            return true;
        }

        private void AdvanceUntil(char c) {
            while (currentIndex < p.Length && p[currentIndex] != c) {
                currentIndex++;
            }
        }

        public bool TryGetStringValue(string key, out string value) {
            value = null;

            if (bad) {
                return false;
            }

            if (!Consume(key)) {
                return false;
            }

            if (!Consume(CookieValueFormat.KeyValueSeparator)) {
                return false;
            }

            int valueStart = currentIndex;
            AdvanceUntil(CookieValueFormat.KeySeparator);
            value = p.Substring(valueStart, currentIndex - valueStart);
            currentIndex++;
            return true;
        }

        public bool TryGetIntValue(string key, out int value) {
            if (!TryGetStringValue(key, out string strValue) || !int.TryParse(strValue, out value)) {
                value = default(int);
                return false;
            }
            return true;
        }

        public bool TryGetBoolValue(string key, out bool value) {
            if (!TryGetStringValue(key, out string strValue) || !bool.TryParse(strValue, out value)) {
                value = default(bool);
                return false;
            }
            return true;
        }

        internal bool TryGetLongValue(string key, out long value) {
            if (!TryGetStringValue(key, out string strValue) || !long.TryParse(strValue, out value)) {
                value = default(long);
                return false;
            }
            return true;
        }
    }

