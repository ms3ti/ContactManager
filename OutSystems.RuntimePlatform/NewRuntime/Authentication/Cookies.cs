        private bool DisableMaxAgeAnonymousCookies(IHttpContext httpContext) {
            bool disableMaxAge = settingsProvider?.Get(RuntimePlatformSettings.Authentication.DisableMaxAgeAnonymousCookies) ?? false;
            if (!disableMaxAge) {
                return false;
            }

            string pattern = settingsProvider?.Get(RuntimePlatformSettings.Authentication.DisableMaxAgeAnonymousCookiesRegex);

            // No need to apply filter to specific user agents
            if (string.IsNullOrEmpty(pattern)) {
                return true;
            }

            // Otherwise, only apply to user agents specified by the regex
            string userAgent = httpContext.Request.UserAgent;
            if (string.IsNullOrEmpty(userAgent)) {
                return false;
            }
            
            try {
                // The timeout is just to avoid hanging, the normal execution time is neglectable 
                return Regex.IsMatch(userAgent, pattern, RegexOptions.IgnoreCase, TimeSpan.FromSeconds(1)); 
            } catch (Exception ex) {
                OSTrace.Error($"[Response cookies] Error applying regex {pattern} to user agent {userAgent}", ex);
                return false;
            }
        }
