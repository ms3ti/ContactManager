        public void ReactiveLogin(int userId, int tenantId, string userName, bool isPersistent) {
            if (!IsSupported()) {
                return;
            }
            OSTrace.Debug($"[SSO] Reactive/Mobile login for user id '{userId}' via Traditional Web login");
            var mobileSessionInfo = GetMobileSessionInfo();
            MobileInfo = mobileSessionInfo.LoginInfo;
            MobileInfo?.Login(userId, tenantId, userName, isPersistent);
            if (settingsProvider.Get(RuntimePlatformSettings.Authentication.SSOSetRolesCookieInTraditionalLogin)) {
                SetRolesInfo(mobileSessionInfo);
            }
            MobileInfo?.WriteLoginInfoToResponse(httpContext);
            CookieDebug.Log(httpContext, MobileInfo, extraInfo: "Login via Traditional");
        }

        public void ReactiveRefreshCookies() {
            if(!IsSupported()) {
                return;
            }
            var mobileSessionInfo = GetMobileSessionInfo();
            MobileInfo = mobileSessionInfo.LoginInfo;
            MobileInfo?.WriteLoginInfoToResponse(httpContext);
            OSTrace.Debug($"[SSO] Reactive/Mobile refresh cookies for user id '{mobileSessionInfo.UserId}' via Traditional Web'");
            CookieDebug.Log(httpContext, MobileInfo, extraInfo: "Refresh via Traditional");
        }

        public void ReactiveLogout() {
            if (!IsSupported()) {
                return;
            }
            MobileInfo?.Logout();
            MobileInfo?.WriteLoginInfoToResponse(httpContext);
            CookieDebug.Log(httpContext, MobileInfo, extraInfo:"Logout via Traditional");
        }

        public void TraditionalPersistentCookieLogin(int userId, bool persistent) {
            if (!IsSupported()) {
                return;
            }
            var cookieActions = new CookieActions(settingsProvider, httpContext, databaseAccess, sessionMetadata, logger);
            cookieActions.AddOrRefreshPersistentLoginCookie(moduleInfo, PersistentSessionInfo.GetPersistentLoginCookieName(moduleInfo, tenantInfo), tenantInfo.Id, userId, !persistent, "InnerLogin");
            OSTrace.Debug($"[SSO] Traditional Web login/refresh for user id '{userId}' via Reactive/Mobile login");
        }

        public void TraditionalSessionLogout() {
            if (!IsSupported()) {
                return;
            }
            var cookieActions = new CookieActions(settingsProvider, httpContext, databaseAccess, sessionMetadata, logger);
            cookieActions.DeleteSessionCookie();
        }

        private bool IsSupported() {
            if (!settingsProvider.Get(RuntimePlatformSettings.Authentication.SSOBetweenRuntimes)) {
                return false;
            }
            if (settingsProvider.Get(RuntimePlatformSettings.Application.IsInIsolatedDB)) {
                OSTrace.Warning("[SSO] SSO between runtimes is not supported when IsolatedDB is enabled");
                return false;
            }
            if (!RuntimePlatformUtils.RequestIsSecure(HttpService.Context.Request)) {
                var warning = "[SSO] SSO could not be supported as connection is not secure";
                OSTrace.Warning(warning);
                logger.LogGeneral(warning, "WARNING", "SSOBetweenRuntimes");
                return false;
            }
            return true;
        }

        protected virtual MobileSessionInfo GetMobileSessionInfo() {
            var runtimeSettingsProvider = RuntimeSettingsProvider.Instance;

            var mobileInfo = LoginInfo.ReadLoginInfoFromRequest(
                                                mobileLoginConfiguration,
                                                new MobileCookieNames(moduleInfo.UserProvider.Name, moduleInfo.Name),
                                                ignoreCSRFToken: true,
                                                hasSessionStorage: true,
                                                runtimeSettingsProvider);

            var mobileSessionInfo = new MobileSessionInfo(
                                            mobileLoginConfiguration,
                                            mobileInfo,
                                            runtimeSettingsProvider,
                                            moduleInfo,
                                            DatabaseAccess.ForSystemDatabase,
                                            sessionMetadata,
                                            null,
                                            logger);
            mobileSessionInfo.Validate();
            return mobileSessionInfo;
        }


        private void SetRolesInfo(MobileSessionInfo mobileSessionInfo) {
            try {
                mobileSessionInfo.UserId = MobileInfo.UserId;
                mobileSessionInfo.TenantId = MobileInfo.TenantId;
                MobileInfo.UpdateRolesHashAndCalculateRolesInfo(mobileSessionInfo.GetAllRoles());
            } catch (Exception ex) {
                OSTrace.Error("There was an error trying to set roles information", ex);
            }
        }
    }
