        // Get names of headers and cookies
        private static Dictionary<Cookie, string> GetCookieNames(LoginInfo loginInfo) {
            var cookieNames = loginInfo.CookieNames;
            var dictionary = new Dictionary<Cookie, string>()
            {
                { Cookie.CsrfToken, cookieNames.CSRFHeaderName },
                { Cookie.HttpCookie, cookieNames.HttpOnlyLoginCookieName },
                { Cookie.UserCookie, cookieNames.UserAccessibleLoginCookieName },
                { Cookie.RememberMe, cookieNames.CookieSuffix },
                { Cookie.OsVisitor, "osVisitor" }
            };
            
            return dictionary;
        }

        private static string DecodeAndSanitizeCookie(Cookie cookie, string cookieValue) {
            var decodedCookie = HttpUtility.UrlDecode(cookieValue ?? "", Encoding.UTF8);

            if (cookie == Cookie.RememberMe) {
                return cookieValue.FormatSensitiveString();
            }

            string[] sensitivePatterns = cookie switch {
                Cookie.HttpCookie => new[] { @"lid=([^;]+)", @"hmc=([^;]+)" },
                Cookie.UserCookie => new[] { @"crf=([^;]+)" },
                _ => Array.Empty<string>()
            };

            var sanitizedCookie = decodedCookie.FormatSensitivePatternsInString(sensitivePatterns);

            return sanitizedCookie;
        }
