        private static void WriteRequestHeaders(StringBuilder trace, IHttpContext httpContext, IDictionary<Cookie, string> cookieNames) {
            trace.AppendLine("Request cookies:");

            var csrfHeaderName = cookieNames[Cookie.CsrfToken];
            if (httpContext.Request.Headers.TryGetValue(csrfHeaderName, out string csrfToken)) {
                trace.AppendLine($"  {csrfHeaderName}: {csrfToken?.FormatSensitiveString()}");
            } else {
                trace.AppendLine($"  {csrfHeaderName} header not found in the request");
            }

            foreach (var cookieKey in cookieNames.Keys.Except(new[] { Cookie.CsrfToken })) {
                var cookieName = cookieNames[cookieKey];
                if (!httpContext.Request.Cookies.TryGetValue(cookieName, out string cookieValue)) {
                    trace.AppendLine($"  {cookieName} cookie not found in the request");
                    continue;
                }

                var cookieValueSanitized = DecodeAndSanitizeCookie(cookieKey, cookieValue);
                trace.AppendLine($"  {cookieName}: {cookieValueSanitized}");
            }
        }

        private static void WriteResponseCookies(StringBuilder trace, IHttpContext httpContext, IDictionary<Cookie, string> cookieNames) {
            trace.AppendLine("Response Set-Cookie:");

            // Response Cookies does not have TryGetValue. We need to iterate keys and access with Cookies[key].
            // Not accessing the cookie with the exact key object causes cookies to be overridden.
