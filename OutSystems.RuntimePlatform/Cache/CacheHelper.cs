using OutSystems.RuntimeCommon.Cryptography.Insecure;

namespace OutSystems.HubEdition.RuntimePlatform {
    // Helper class to build keys used by .net cache system.
    // Appended keys are separated by a | and the hash of that key is to index the value in the cache
    public class CacheHelper {
        private const string Separator = "\x05";
        private string key;

        public CacheHelper() {
            key = string.Empty;
        }

        public string GetKey() {
            return key;
        }

        public void AddValue(string key) {
            this.key += key + Separator;
        }

        public void AddValue(int key) {
            AddValue(key.ToString());
        }

        public string GetHash() {
            //#RRCT-3285 Change to ToGuid that uses Encoding.UTF8.Encode underneath instead of default encoding that can change
            //between different machines
            return MD5GuidHelper.ToGuid(key).ToString();
        }
    }

}
