#pragma warning restore 618
                if (setting.Key != fullKeyName) {
                    settingValue = GetSettingValue(fullKeyName);
                }
            }

            return LoadFromString(setting, settingValue);
        }

        public static void WithSettingsOverride(IDictionary<string, object> settings, Action action) {
            OverridenSettings.Push(settings);

            try {
                action();
            } finally {
                OverridenSettings.Pop();
            }
        }

        internal T LoadFromString<T>(ISetting<T> setting, string settingValue) {
            if (setting.IsEncrypted) {
                settingValue = SecureConfidentialInformationEncryption.TryDecryptString(settingValue);
            }

            if (settingValue == null) {
                return setting.DefaultValue;
            }

            settingValue = settingValue.Trim(); // Keeping compatibility with the old Settings behavior
            try {
                Type t = typeof(T);
                t = Nullable.GetUnderlyingType(t) ?? t;

                return settingValue == null
                    ? setting.DefaultValue
                    : (T)Convert.ChangeType(settingValue, t, CultureInfo.InvariantCulture);
            } catch (FormatException) {
                // #RPD-4296 - Added logs
                var appInfo = AppInfo.GetAppInfo();
                if (appInfo?.OsContext != null) {
                    EventLogger.WriteWarning($"Application {appInfo.ApplicationName}: Error loading setting '{setting.Key}' from string");
                } else {
                    EventLogger.WriteWarning($"Error loading setting '{setting.Key}' from string");
                }
            }

            // Keeping compatibility with the old Settings behavior
            return setting.DefaultValue;
        }

