        private static volatile Dictionary<Pair<string, string>, int> _roleIdCache;

        public int GetRoleIdFromKey(string ssKey, string espaceKey) {
            var key = new Pair<string, string>(ssKey, espaceKey);
            if (_roleIdCache == null || !_roleIdCache.ContainsKey(key)) {
                lock (typeof(HeContext)) {
                    if (_roleIdCache == null || !_roleIdCache.ContainsKey(key)) {
                        using (var tran = DatabaseAccess.ForSystemDatabase.GetReadOnlyTransaction()) {
                            using (var reader = DBRuntimePlatform.Instance.GetRoleKeyIdTable(tran)) {
                                var cache = new Dictionary<Pair<string, string>, int>();
                                while (reader.Read()) {
                                    cache[new Pair<string, string>(reader.SafeGet<string>(0), reader.SafeGet<string>(1))] = Convert.ToInt32(reader[2]);
                                }
                                _roleIdCache = cache;
                            }
                        }
                    }
                }
            }
            if (!_roleIdCache.TryGetValue(key, out int id)) {
                throw new InvalidOperationException("Unknown role");
            }
            return id;
        }

