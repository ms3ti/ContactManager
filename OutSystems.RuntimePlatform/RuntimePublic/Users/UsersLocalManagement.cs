        private static void SetAssociation(string tableName, string mainIdColumnName, string AssociationIdColumnName, int mainId, int AssociationId) {
            using (var trans = DatabaseAccess.ForRuntimeDatabase.GetCommitableTransaction()) {
                var tenantId = GetCurrentTenantId();
                var sql = $@"SELECT COUNT({mainIdColumnName})
                        FROM {tableName}
                        WHERE {mainIdColumnName}=@MAIN_ID AND {AssociationIdColumnName}=@ASSOCIATION_ID AND TENANT_ID=@TENANT_ID";
                using (var cmd = trans.CreateCommand(sql)) {
                    cmd.CreateParameter("@MAIN_ID", DbType.Int32, mainId);
                    cmd.CreateParameter("@ASSOCIATION_ID", DbType.Int32, AssociationId);
                    cmd.CreateParameter("@TENANT_ID", DbType.Int32, tenantId);

                    if (cmd.ExecuteScalar<int>() > 0) {
                        return;
                    }
                }
                sql = $@"INSERT INTO {tableName}({mainIdColumnName}, {AssociationIdColumnName}, TENANT_ID)
                        VALUES
                        (@MAIN_ID, @ASSOCIATION_ID, @TENANT_ID)";

                using (var cmd = trans.CreateCommand(sql)) {
                    cmd.CreateParameter("@MAIN_ID", DbType.Int32, mainId);
                    cmd.CreateParameter("@ASSOCIATION_ID", DbType.Int32, AssociationId);
                    cmd.CreateParameter("@TENANT_ID", DbType.Int32, tenantId);

                    cmd.ExecuteNonQuery();
                }

                trans.Commit();
            }
        }

