        private static IEnumerable<IEnumerator<T>> Batch<T>(this IEnumerable<T> source, int batchSize) {
            using (var enumerator = source.GetEnumerator()) {
                while (enumerator.MoveNext()) {
                    yield return YieldBatchElements(enumerator, batchSize - 1);
                }
            }
        }

        private static IEnumerator<T> YieldBatchElements<T>(IEnumerator<T> source, int batchSize) {
            yield return source.Current;
            for (int i = 0; i < batchSize && source.MoveNext(); i++) {
                yield return source.Current;
            }
        }

        private static IEnumerable<T> ToIEnumerable<T>(this IEnumerator<T> enumerator) {
            while (enumerator.MoveNext()) {
                yield return enumerator.Current;
            }
        }

