            return $@"SELECT {idColumnName}, {recordColumntring}
                                        FROM 
                                        (
                                            VALUES
                                                {string.Join(
                                                    ",",
                                                    recordsValues
                                                    .Select(
                                                        (str, idx) =>
                                                            $"({recordsIds[idx]}, {str})"))}
                                        ) SOME_NAME ({idColumnName}, {recordColumntring})";
        }

        private static void DoBulkAssociation(string tableName, string mainIdColumnName, string associationIdColumnName, int mainId, IEnumerable<int> associationIds, bool ignoreNonExistingForeignKeys = false, string associationTableName = "", string associationTableIdColumnName = "") {
            var tenantId = GetCurrentTenantId();
            using (var trans = DatabaseAccess.ForRuntimeDatabase.GetCommitableTransaction()) {
                Command cmd = null;

                if (!associationIds.Any()) {
                    var sql = $@"DELETE FROM {tableName}
                            WHERE 
                                {mainIdColumnName} = @MAIN_ID AND TENANT_ID = @TENANT_ID";

                    using (cmd = trans.CreateCommand(sql)) {
                        cmd.CreateParameter("@MAIN_ID", DbType.Int32, mainId);
                        cmd.CreateParameter("@TENANT_ID", DbType.Int32, tenantId);
                        cmd.ExecuteNonQuery();
                    }

                    trans.Commit();
                    return;
                }

                var recordsSelects = new StringBuilder();
                var idsSelects = new StringBuilder();
                var deleteValues = new StringBuilder();
                bool isFirst = true;
                var batches = associationIds.Batch(BatchSize);

                foreach (var batchLazy in batches) {
                    var batchData = batchLazy.ToIEnumerable().ToArray();
                    var AssociationIdValues = "(" + string.Join(",", batchData) + ")";

                    var recordIdValues = DBRuntimePlatform.Instance.GetSelectForValues(batchData, associationIdColumnName);

                    if (isFirst) {


                        recordsSelects.Append(recordIdValues);

                        idsSelects.Append($@"SELECT DISTINCT({associationIdColumnName})
                                        FROM {tableName}
                                        WHERE
                                            {mainIdColumnName} = @MAIN_ID AND TENANT_ID = @TENANT_ID AND
                                            ({associationIdColumnName} IN {AssociationIdValues}");

                        isFirst = false;
                    } else {

                        recordsSelects.Append($@" UNION(
                                                {recordIdValues}
                                            )");

                        idsSelects.Append($@" OR {associationIdColumnName} IN {AssociationIdValues}");
                    }
                    deleteValues.Append($@" AND {associationIdColumnName} NOT IN {AssociationIdValues}");
                }

                idsSelects.Append(")");
                var deleteSql = $@"DELETE FROM {tableName}
                                WHERE 
                                    {mainIdColumnName} = @MAIN_ID AND TENANT_ID = @TENANT_ID {deleteValues}";

                var insertSql = $@"INSERT INTO {tableName} ({mainIdColumnName}, {associationIdColumnName}, TENANT_ID)
                                SELECT @MAIN_ID {mainIdColumnName}, {associationIdColumnName}, @TENANT_ID TENANT_ID
                                FROM 
                                (
                                    {recordsSelects}
                                ) SOME_NAME
                                WHERE 
                                    SOME_NAME.{associationIdColumnName} NOT IN 
                                    (
                                        {idsSelects}
                                    )";

                if (ignoreNonExistingForeignKeys) {
                    insertSql += $@" AND 
                                    SOME_NAME.{associationIdColumnName} IN 
                                    (
                                        SELECT {associationTableIdColumnName} from {associationTableName}
                                    )";
                }

                using (cmd = trans.CreateCommand(deleteSql)) {
                    cmd.CreateParameter("@MAIN_ID", DbType.Int32, mainId);
                    cmd.CreateParameter("@TENANT_ID", DbType.Int32, tenantId);
                    cmd.ExecuteNonQuery();
                }

                using (cmd = trans.CreateCommand(insertSql)) {
                    cmd.CreateParameter("@MAIN_ID", DbType.Int32, mainId);
                    cmd.CreateParameter("@TENANT_ID", DbType.Int32, tenantId);
                    cmd.ExecuteNonQuery();
                }

                trans.Commit();
            }
        }

