using System;
using System.Text.RegularExpressions;
using OutSystems.RuntimeCommon;

namespace OutSystems.Application.Core {

    public static class LogServiceExtensions {

        /// <summary>
        /// Format a sensitve string in a way that the complete value is unknown.
        /// It is useful to prevent logged information to be used in attacks.
        /// NOTE: this must not be used in passwords! Knowing part of the password is still a relevant leak.
        /// </summary>
        /// <param name="value"></param>
        /// <param name="visibleFactor">Amount of the string that is kept visible. Between 0 and 1. Default is 0.5, meaning half will be kept untouched.</param>
        /// <returns>A transformation of the input value, partially replaced by '*' characters.</returns>
        public static string FormatSensitiveString(this string value, double visibleFactor = 0.5) {
            if (visibleFactor > 1 || visibleFactor < 0) {
                throw new ArgumentException($"Invalid {nameof(visibleFactor)}");
            }

            if (value.IsNullOrEmpty()) {
                return string.Empty;
            }

            var visibleLen = (int)Math.Truncate(value.Length * visibleFactor);
            return $"{value.Substring(0, visibleLen)}{new string('*', value.Length - visibleLen)}";
        }

        public static string FormatSensitivePatternsInString(this string value, string[] regexPatterns, double visibleFactor = 0.5) {
            if (regexPatterns == null) {
                return value;
            }
            foreach (var pattern in regexPatterns) {
                Match match;
                try {
                    match = Regex.Match(value, pattern,RegexOptions.None, TimeSpan.FromSeconds(1));
                } 
                catch {
                    continue;
                }

                if (match.Success) {
                    string sensitiveInfo = match.Groups[1].Value;
                    string formattedSensitiveInfo = sensitiveInfo.FormatSensitiveString(visibleFactor);

                    value = value.Replace(sensitiveInfo, formattedSensitiveInfo);
                }
            }

            return value;
        }

    }

