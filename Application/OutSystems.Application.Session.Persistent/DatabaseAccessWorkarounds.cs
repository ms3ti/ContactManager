using System;
using System.Web;
using OutSystems.RuntimeCommon.Settings;

namespace OutSystems.Application.Session.Persistent {
    class DatabaseAccessWorkarounds {
        // Oracle Managed ODP driver gets stuck forever if we stop it with a request abort in the middle of a query so we disable the request timeout when we are executing queries.

        // The new Cancelation Token based request timeout mechanism doesn't have this problem since it doesn't stop queries in the middle of execution but we can't use it
        // to retrive the session #RRCT-2360.

        internal DatabaseAccessWorkarounds() {
        }

        internal T ExecuteWithoutRequestTimeout<T>(Func<T> action) {

            if (HttpContext.Current == null) {
                return action();
            }

            int oldScriptTimeout = HttpContext.Current.Server.ScriptTimeout; // if HttpContext.Current is not null then HttpContext.Current.Server is also not null

            if (oldScriptTimeout == int.MaxValue) {
                return action(); // Already at max timeout. No point in changing it again.
            }

            try {
                // ScriptTimeout requires a positive number so we need to set to int.MaxValue to "disable" request timeouts
                // https://forums.asp.net/t/1925766.aspx?Httpruntime+ExecutionTimeOut+
                // https://referencesource.microsoft.com/#system.web/httpserverutility.cs,f8f47afb04ad1133,references
                HttpContext.Current.Server.ScriptTimeout = int.MaxValue;
                return action();
            } finally {
                HttpContext.Current.Server.ScriptTimeout = oldScriptTimeout;
            }

        }

    }
}
