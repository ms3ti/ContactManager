        public void ValidateSessionFixationCookieAgainstSession(IModuleInfo moduleInfo, PersistentSessionInfo sessionInfo) {
            if (sessionInfo.IsReadOnlySession) {
                // If we are in a readonly session we must not chande the session fixation cookie.
                // Currently we are also skiping the validation too to maintain compatibility with old semantic
                return;
            }

            // Process SessionFixationProtection cookie
            if (SettingsProvider.Get(SessionSettings.EnableSessionFixationProtection)) {
                string protectionValueInSession = sessionInfo.SessionFixationProtection;
                if (protectionValueInSession != null && sessionInfo.PendingSessionProtectionValidation) {
                    var protectionValueCookie = GetCookieValueByName(HttpContext.Request.Cookies, sessionInfo.SessionFixationProtectionCookieName);

                    sessionInfo.PendingSessionProtectionValidation = false;

                    if (protectionValueCookie == null || !protectionValueInSession.Equals(protectionValueCookie)) {
                        Log("Session fixation mismatch with current cookie (session:" + protectionValueInSession + " cookie:" + (protectionValueCookie ?? "null") + ")");
                        var protectionValueCookieValue = protectionValueCookie ?? "";
                        if (DateTime.UtcNow < sessionInfo.SessionFixationProtectionExpirationInstant && sessionInfo.SessionFixationProtectionOld.Equals(protectionValueCookieValue)) {
                            if (SettingsProvider.Get(SessionSettings.AllowSessionFixationCookieRefresh)) {
                                // Previous session fixation cookie still accepted within the timeframe
                                // Send current cookie again
                                SecureCookieUtils.SetSessionCookie(SettingsProvider, HttpContext.Response.Cookies,
                                    sessionInfo.SessionFixationProtectionCookieName,
                                    sessionInfo.SessionFixationProtection,
                                    new CookieOptions { Path = SESSION_FIXATION_PROTECTION_COOKIE_PATH });
                            }
                            Log("(repeat) CurrentSessionFixationProtectionCookie (" + sessionInfo.SessionFixationProtection + ")");
                            return;
                        }
                        Log("Session fixation mismatch with old cookie (old:" + sessionInfo.SessionFixationProtectionOld + " cookie:" + (protectionValueCookie ?? "null") + ")");
                        // Clear the session cookie to avoid locking the user to an invalid session in the next request. This might not work when we have session cookies bound to the domain,
                        // but it would help the rare case when people get locked with no reason to simply retry the request and continue.
                        DeleteSessionCookie();
                        if (SettingsProvider.Get(SessionSettings.EnableLogoutOnSessionFixationMismatch)) {
                            sessionInfo.Logout();
                        }
                        throw new SessionFixationException("Session fixation mismatch");
                    }
                }
            }
        }

        public void AddSessionFixationProtectionCookie(PersistentSessionInfo sessionInfo) {
            if (SettingsProvider.Get(SessionSettings.EnableSessionFixationProtection)) {
                string randomPart = string.Empty + GetRandomLongNumber() + GetRandomLongNumber();
                string protectionValueCookie = GetCookieValueByName(HttpContext.Request.Cookies, sessionInfo.SessionFixationProtectionCookieName) ?? string.Empty;
                sessionInfo.SessionFixationProtectionOld = protectionValueCookie;
                sessionInfo.SessionFixationProtection = randomPart;
                sessionInfo.PendingSessionProtectionValidation = false;
                SecureCookieUtils.SetSessionCookie(SettingsProvider, HttpContext.Response.Cookies,
                    sessionInfo.SessionFixationProtectionCookieName, randomPart,
                    new CookieOptions { Path = SESSION_FIXATION_PROTECTION_COOKIE_PATH });
                Log("OldSessionFixationProtectionCookie (" + sessionInfo.SessionFixationProtectionOld + "); NewSessionFixationProtectionCookie(" + sessionInfo.SessionFixationProtection + ")");
                sessionInfo.SessionFixationProtectionExpirationInstant = DateTime.UtcNow.AddSeconds(SettingsProvider.Get(SessionSettings.SessionFixationAllowedOverlapWindowInSecs));
            }
        }

