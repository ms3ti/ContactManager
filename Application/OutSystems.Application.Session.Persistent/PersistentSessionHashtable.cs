                GetEspaceBag(eSpaces, eSpace)[key] = objToStore;
            } else {
                info.AddValue(key, objToStore);
            }
        }

        private IDictionary<string, object> GetEspaceBag(IDictionary<string, IDictionary<string, object>> eSpaces, string eSpace) {
