            public static T ExecuteWithMutex<T>(string mutexName, bool globalMutex, Func<T> function) {
                using Mutex mutex = CreateMutex((globalMutex ? "Global\\" : "") + mutexName);
                bool adquired = false;
                try {
                    adquired = mutex.WaitOne();
                    return function();
                } finally {
                    if (adquired) {
                        mutex.ReleaseMutex();
                    }
                }
