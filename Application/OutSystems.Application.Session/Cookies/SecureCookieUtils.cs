namespace OutSystems.Application.Session.Cookies {
    public static class SecureCookieUtils {
        public static void SetSessionCookie(ISettingsProvider settingsProvider, IResponseCookies responseCookies, string name, string value, CookieOptions options = null) {
            options ??= new CookieOptions();

            if (options.HttpOnly != true && settingsProvider.Get(SessionSettings.EnforceSessionCookiesHttpOnly)) {
                options.HttpOnly = true;
            }

            if (options.Secure != true && settingsProvider.Get(SessionSettings.EnforceSessionCookiesSecure)) {
                options.Secure = true;
            }

