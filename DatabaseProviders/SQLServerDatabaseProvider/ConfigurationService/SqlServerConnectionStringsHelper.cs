namespace OutSystems.HubEdition.DatabaseProvider.SqlServer.ConfigurationService {
    public class SqlServerConnectionStringsHelper {

        static readonly Regex multipleActiveResultSetsRegex = new Regex(@"multipleactiveresultsets\s*=", RegexOptions.CultureInvariant | RegexOptions.IgnoreCase);
        public static string AppendMultipleActiveResultSetsTrueIfNeeded(bool needsFix, string connectionString) {
            if (!needsFix) {
                return connectionString;
            }

            // #RSAT-1918 - changed the check != null for a !string.IsNullOrEmpty
            if (!string.IsNullOrEmpty(connectionString) && !multipleActiveResultSetsRegex.IsMatch(connectionString)) {
                if (!connectionString.EndsWith(";")) {
