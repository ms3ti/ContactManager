    public class TransactionManager : GenericTransactionManager {

        public TransactionManager(ITransactionService transactionService) : base(transactionService) { }

        // RPD-4236: SQL Server plugin does not support multiple active result sets (ProviderProperties.SupportsMultipleActiveResultSets == false)
