namespace OutSystems.HubEdition.DatabaseProvider.PostgreSQL.TransactionService {
    class TransactionService : BaseTransactionService {
        public TransactionService(IDatabaseServices services) : base(services) { }

        public override bool NeedsSeparateAdminConnection => true;

        protected override IsolationLevel IsolationLevel => IsolationLevel.ReadCommitted;

        protected override IDbConnection GetConnectionFromDriver() {
            NpgsqlConnection conn = null;
            try {
                conn = new NpgsqlConnection(DatabaseServices.DatabaseConfiguration.ConnectionString);
                return conn;
            } catch {
                if (conn != null) {
                    conn.Dispose();
                }
                throw;
            }
        }

        protected override IDbConnection GetConnection() {
            var conn = base.GetConnection();
            ValidateConnectionOpen(conn);
            return conn;
        }

        private static void ValidateConnectionOpen(IDbConnection conn) {
            //Execute Canary Query to validate the connection is valid because the Npgsql driver does not guaranty the conncetion is valid.
            //This is preferable to use instead of the KeepAlive=<seconds> which sends a SELECT 1 query every <seconds> period which
            //might be a performance hit.
            string statement = "SELECT 1";
            using (var cmd = new NpgsqlCommand(statement, (NpgsqlConnection)conn)) {
                cmd.ExecuteScalar();
            }
        }

        protected override void ReleaseAllPooledConnections() {
            NpgsqlConnection.ClearAllPools();
        }

        public override ITransactionManager CreateTransactionManager() {
            return new TransactionManager(this);
        }
    }
