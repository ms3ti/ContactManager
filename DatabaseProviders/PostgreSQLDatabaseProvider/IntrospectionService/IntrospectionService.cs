            tableConditionVal = null;
            tableConditionParam = null;

            var tableSourceList = tableSources.Select(x => x.ToLowerInvariant()).ToArray();

            if (tableSourceList.IsSingle()) {
                tableConditionVal = tableSourceList.First();
                tableConditionParam = "TABLENAME";
                tableCondition += " = lower(" + paramPrefix + tableConditionParam + ")";
            } else {
                tableCondition += " = ANY (array ['" + tableSourceList.StrCat("','") + "'])";
            }
            return tableCondition;
        }

        protected string GetQualifiedTableName(DatabaseInfo databaseInfo, string tableName) {
            IDMLIdentifiers identifiers = DatabaseServices.DMLService.Identifiers;
            return string.Format("{0}.{1}",
                identifiers.EscapeIdentifier(databaseInfo.Schema),
                identifiers.EscapeIdentifier(tableName));
        }
