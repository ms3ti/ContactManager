        static private string GetForeignKeysParameterizedQuery(IEnumerable<TableSourceInfo> tableSources, string paramPrefix, out string schemaNameParam, out string tableConditionParam, out string tableConditionVal) {
            schemaNameParam = SCHEMA_NAME_PARAM;

            string tableCondition = GenerateTableCondition("r.relname", true, tableSources.Select(ts => ts.Name), paramPrefix, out tableConditionParam, out tableConditionVal);

            /*
             * 1nd Find the constraint (constraint_name, delete_rule) information and column information of the referenced table (r)  
             * for the given table(s) (r2) - ccu (constraint_column_usage)
             * 2nd Find the column information of foreign keys for the given table(s) - kcu (key_column_usage)
             * Matching between kcu and ccu is done using contraint OIDs to be more efficient.
             * 
             * For additional info check the comments in the query itself.
             * */

            return $@"
            with local_pg_class(oid, relname, relnamespace) -- For efficiency and reutilization. Represents the given Table(s) information.
            AS
            (
	            select oid, relname, relnamespace
	            from pg_class r
	            where r.relnamespace = to_regnamespace({paramPrefix}{schemaNameParam})
	                AND (r.relkind = ANY (ARRAY['r'::""char"", 'p'::""char""]))
	 	            AND {tableCondition}
            )
            SELECT DISTINCT
                ccu.table_schema,
                ccu.table_name,
                kcu.column_name,
                ccu.constraint_name,
                ccu.foreign_table_schema AS foreign_table_schema,
                ccu.foreign_table_name AS foreign_table_name,
                ccu.foreign_column_name AS foreign_column_name,
                ccu.delete_rule
            FROM  (
                SELECT
                    c.oid As constraint_coid,
                    a.attname AS column_name
                FROM
                    pg_namespace nr,
                    local_pg_class r,
                    pg_namespace nc,
                    pg_constraint c,
                    pg_attribute a
                WHERE 
                    (nr.oid = r.relnamespace)
                    AND (r.oid = c.conrelid)
                    AND (nc.oid = c.connamespace)
                    AND (c.contype = 'f'::""char"")
                    AND r.oid = a.attrelid
                    AND array_position(c.conkey,a.attnum) IS NOT NULL
                    AND (NOT a.attisdropped)
			) AS kcu
            INNER JOIN (
                SELECT
                    x.table_name,
                    {paramPrefix}{schemaNameParam} AS table_schema,
                    x.tblschema AS foreign_table_schema,
                    x.tblname AS foreign_table_name,
                    x.colname AS foreign_column_name,
                    x.cstrname AS constraint_name,
                    x.conrelid AS conrelid,
                    x.connamespace as connamespace,
                    x.confdeltype as delete_rule,
                    x.coid as constraint_oid
                FROM (
                    SELECT
                        r2.relname,
                        nr.nspname,
                        r.relname,
                        r.relowner,
                        a.attname,
                        nc.nspname,
                        c.conname,
                        c.conrelid,
                        c.connamespace,
                        c.confdeltype,
                        c.oid
                    FROM 
                        pg_namespace nr,
                        pg_class r,
                        local_pg_class r2,
                        pg_attribute a,
                        pg_namespace nc,
                        pg_constraint c
                    WHERE 
                        (nr.oid = r.relnamespace)
                        AND (r.oid = a.attrelid)
                        AND (nc.oid = c.connamespace)
                        AND (r.oid =
                            CASE c.contype
                                WHEN 'f'::""char"" THEN c.confrelid
                                ELSE c.conrelid
                            END)
                        AND (c.conrelid = r2.oid) 
                        AND (c.contype = 'f'::""char"")  --'FOREIGN KEY'
                        AND (a.attnum = ANY (
                            CASE c.contype
	                            WHEN 'f'::""char"" THEN c.confkey
	                            ELSE c.conkey
                            END))
                        AND (NOT a.attisdropped)
                ) x(table_name, tblschema, tblname, tblowner, colname, cstrschema, cstrname,conrelid,connamespace,confdeltype,coid)
			) AS ccu
            ON 
                ccu.constraint_oid = kcu.constraint_coid
            ORDER BY
                ccu.table_name
            ";
        }

