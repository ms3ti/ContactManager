        private string GenerateSqlStatement(IEnumerable<string> tableSourcesNames, out string schemaNameParam, out string tableConditionParam, out string tableConditionVal, bool isCount, bool isOrdered, bool isPaginated, int offset, int limit) {
            schemaNameParam = SCHEMA_NAME_PARAM;
            string tableAndViewCondition;
            string paramPrefix = DatabaseServices.ExecutionService.ParameterPrefix;
            string schemaNameWithPrefix = paramPrefix + schemaNameParam;

            if (!tableSourcesNames.IsNullOrEmpty()) {
                tableAndViewCondition = GenerateTableCondition("table_name", true, tableSourcesNames, paramPrefix, out tableConditionParam, out tableConditionVal);
            } else {
                tableConditionParam = null;
                tableConditionVal = null;
                tableAndViewCondition = "TRUE";
            }
            string countOpen = isCount ? "SELECT COUNT(*) FROM (" : "";
            string countClose = isCount ? ") AS derivedTable" : "";
            string union =
            $@"SELECT table_name 
            FROM information_schema.tables 
            WHERE table_schema = {schemaNameWithPrefix} 
            AND table_type = 'BASE TABLE' AND {tableAndViewCondition}
            UNION 
            SELECT table_name 
            FROM information_schema.views 
            WHERE table_schema = {schemaNameWithPrefix} AND {tableAndViewCondition}";
            string order = isOrdered ? "ORDER BY table_name" : "";
            string pagination = isPaginated ? $"OFFSET {offset} LIMIT {limit};" : "";

            return $"{countOpen} {union} {order} {pagination} {countClose}";
        }

