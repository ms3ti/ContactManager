        /// Bulk inserts data into the database. Postgresql way.
        /// </summary>
        /// <param name="datatable">A datatable with all information to transfer.</param>
        public override void BulkInsert(DataTable datatable) {
            bool useFallback = false;

            bool anyRecordsToImport = false;
            //Avoid an array or list copy operation.
            //We only want to know if there is at least one record to be imported/inserted.
            foreach (var dataRow in datatable.Rows) {
                if (((DataRow)dataRow).RowState == DataRowState.Added
                    && string.IsNullOrEmpty(((DataRow)dataRow).RowError)) {
                    anyRecordsToImport = true;
                    break;
                }
            }

            if (!anyRecordsToImport) {
                return;
            }

            using (var connection = (NpgsqlConnection)DatabaseServices.TransactionService.CreateConnection()) {
                string columns = string.Empty;
                string prepend = string.Empty;
                foreach (DataColumn col in datatable.Columns) {
                    columns += prepend + DatabaseServices.DMLService.Identifiers.EscapeIdentifier(col.ColumnName);
                    prepend = ",";
                }

                string bulkInsertQuery = $"COPY {datatable.TableName} ( {columns} ) FROM STDIN(FORMAT BINARY)";

                using (var writer = connection.BeginBinaryImport(bulkInsertQuery)) {
                    try {
                        foreach (DataRow row in datatable.Rows) {
                            try {
                                if (row.RowState == DataRowState.Added && string.IsNullOrEmpty(row.RowError)) {
                                    row.RowError = string.Empty;

                                    writer.StartRow();

                                    foreach (DataColumn col in datatable.Columns) {
                                        var typeAndValue = this.TransformRuntimeToDatabaseValue(col.DataType, row[col]);
                                        writer.Write(typeAndValue.Item2, typeAndValue.Item1);
                                    }

                                }
                            } catch (Exception e) {
                                row.RowError = e.Message;
                            }
                        }
                        writer.Complete();
                        datatable.AcceptChanges();

                    } catch (Npgsql.PostgresException e) {
                        OSTrace.Error("Exception writing data using Bulk Copy to table " + datatable.TableName + " : " + e.Message +
                           ". Falling back to INSERT query, one by one.");
                        useFallback = true;

                    } finally {
                        writer.Close();
                    }
                }
            }
            //fallback to try one by one
            if (useFallback) {
                this.BulkInsertFallback(datatable);
            }
        }

