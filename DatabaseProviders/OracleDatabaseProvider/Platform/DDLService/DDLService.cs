        const string ORACLE_SEQUENCE_CACHE_SIZE_PARAMETER = "OutSystems.HubEdition.OracleSequenceCacheSize";
        const string defaultCacheSize = "CACHE 100";
        const string maximumCacheSize = "CACHE 1000";
        const string noCache = "NOCACHE";
        public String GetSequenceCacheSize() {         
            string paramPrefix = DatabaseServices.ExecutionService.ParameterPrefix;

            using (IDbConnection conn = DatabaseServices.TransactionService.CreateConnection()) {
                using (IDbCommand cmd = DatabaseServices.ExecutionService.CreateCommand(conn,
                    @$"SELECT VAL
                    FROM OSSYS_PARAMETER
                    WHERE NAME = {paramPrefix}NAME")) {

                    var param = cmd.CreateParameter();
                    param.ParameterName = $"{paramPrefix}NAME";
                    param.DbType = DbType.String;
                    param.Value = ORACLE_SEQUENCE_CACHE_SIZE_PARAMETER;
                    cmd.Parameters.Add(param);
                    string cacheValueFromDB;

                    try {
                        cacheValueFromDB = cmd.ExecuteScalar<string>();
                    } catch (OracleException) {
                        //For example: ORA-00942 - Table or view does not exists
                        //DatabaseProviders must be Platform independent, that means the ossys_parameter table might not exist, in which case this method should not fail.
                        return noCache;
                    }

                    //cacheValueFromDB is a string so trying to convert to ensure that the return is a valid number
                    if (int.TryParse(cacheValueFromDB, out int cacheSize)) {
                        if (cacheSize <= 1) {
                            return noCache;
                        } else if (cacheSize > 1000) {
                            //setting a maximum value of 1K to avoid issues with very large caches
                            return maximumCacheSize;
                        }
                        return $"CACHE {cacheValueFromDB}";
                    }
                    return defaultCacheSize;
                }
            }
