                        "select count(1) from " + Identifiers.EscapeAndQualifyIdentifierForLocalDatabase(existingColumn.TableSource.Name) + filter)) {
                        return cmd.ExecuteScalar<int>() > 0;
                    }
                }
            }

            if (!existingColumn.IsAutoGenerated && newColumn.IsAutoGenerated
                // Change is allowed if the table is empty
                && hasRows()) {
                errorMessage = GetColumnCannotBeChangedMessage(existingColumn.TableSource.Name, existingColumn.Name,
                    existingColumn.DataType.SqlDataType, existingColumn.DataType.Type.ToText(), "Autonumber",
                    newColumn.DataType.Type.ToText(), false);
                return false;
            }

            // Check if ORA-01440 would be raised
            if (tryParseNumericColumnPrecision(existingColumn, out int existingColumnPrecision) && tryParseNumericColumnPrecision(newColumn, out int newColumnPrecision)
                && (existingColumnPrecision > newColumnPrecision || existingColumn.DataType.Decimals > newColumn.DataType.Decimals || newColumn.DataType.Decimals - existingColumn.DataType.Decimals > newColumnPrecision - existingColumnPrecision)
                && hasRows(filterOutNulls: true)) {
                errorMessage = GetColumnCannotBeChangedMessage(existingColumn, newColumn, false) +
                    " This is because the column must be empty to decrease its precision or scale." +
                    // from MessageId.AlterColumn_CouldNotReduceLength which was shown when error was raised during deployment
                    " Consider creating a new attribute and logic to migrate existing values.";
                return false;
            }

            return true;

        }

