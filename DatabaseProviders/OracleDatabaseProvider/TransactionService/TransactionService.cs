        private void IncludeExtraConfigurations(IDbConnection conn) {
            IDbCommand comm = conn.CreateCommand();

            string nlsLengthSemantics = DatabaseConfiguration.NLS_Length_Semantics.ToString().ToUpper();

            string commandText = $"ALTER SESSION SET NLS_LENGTH_SEMANTICS = '{nlsLengthSemantics}' NLS_NUMERIC_CHARACTERS='.,' ";

            string nlsLanguage = DatabaseConfiguration.NLS_Language;
            if (!string.IsNullOrEmpty(nlsLanguage)) {
                commandText += $" NLS_LANGUAGE='{nlsLanguage}'";
            }

            string nlsTerritory = DatabaseConfiguration.NLS_Territory;
            if (!string.IsNullOrEmpty(nlsTerritory)) {
                commandText += $" NLS_TERRITORY='{nlsTerritory}'";
            }

            if (!string.IsNullOrEmpty(DatabaseConfiguration.Schema)) {
                commandText += $" CURRENT_SCHEMA=\"{DatabaseConfiguration.Schema}\"";
            }

            if (!string.IsNullOrEmpty(DatabaseConfiguration.NLS_Sort)) {
                var nlsComp = DatabaseConfiguration.NLS_Sort == "BINARY" ? "BINARY" : "LINGUISTIC";
                commandText += $" NLS_COMP={nlsComp} NLS_SORT={DatabaseConfiguration.NLS_Sort}";
            }

            // #570898 - avoid ORA-00054 in oracle 11+ by changing ddl_lock_timeout
            // to the same value as DDLLockTimeout configuration parameter 
