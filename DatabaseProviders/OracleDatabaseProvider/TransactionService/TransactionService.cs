                commandText += $" DDL_LOCK_TIMEOUT={DatabaseConfiguration.DDLLockTimeout.Value}";
            }

            comm.CommandText = commandText;
            comm.CommandTimeout = 10 /*seconds*/;
            comm.ExecuteNonQuery();
        }

