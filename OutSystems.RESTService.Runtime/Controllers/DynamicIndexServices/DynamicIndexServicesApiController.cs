namespace OutSystems.RESTService.Controllers {
    public abstract class DynamicIndexServicesApiController : RestServiceApiController {

        protected abstract string ModuleName { get; }

        private static readonly ConcurrentDictionary<string, string> IndexFilesCache = new ConcurrentDictionary<string, string>();

        private string originalIndexFileCache;
        private string OriginalIndexFile {
            get {
                if (originalIndexFileCache == null) {
                    originalIndexFileCache = File.ReadAllText(System.Web.Hosting.HostingEnvironment.MapPath("~/indexDynamic.html"));
                }
                return originalIndexFileCache;
            }
        }

        private string GetIndexHtmlContent(string basePath) {
            basePath = basePath ?? "";
            var content = "";
            if (!IndexFilesCache.TryGetValue(basePath, out content)) {
                content = OriginalIndexFile.Replace("_BASEPATH", basePath);
                IndexFilesCache[basePath] = content;
            }
            return content;
        }

        [HttpGet]
        [Route("")]
        [ResponseType(typeof(string))]
        public HttpResponseMessage PrepareIndex() {
            if ($"/{ModuleName}".EqualsIgnoreCase(Request.RequestUri.LocalPath)) {
                var movedResponse = Request.CreateResponse(HttpStatusCode.Moved);
                var uriBuilder = new UriBuilder(Request.RequestUri);
                uriBuilder.Path = $"/{ModuleName}/";
                movedResponse.Headers.Location = uriBuilder.Uri;
                return movedResponse;
            }

            var response = new HttpResponseMessage();
            string basePath = null;

            // When ISAPI is not installed IsActiveInRequest is false            
            if (OsISAPIFilter.Instance.IsActiveInRequest(HttpContext.Current.Request)) {
                var suffix = OsISAPIFilter.Instance.GetHostSuffix();
                if (!String.IsNullOrEmpty(suffix)) {
                    basePath = suffix;
                }

                var path = OsISAPIFilter.Instance.GetPath();
                if (!String.IsNullOrEmpty(path)) {
                    basePath += path;
                }
            } else {
                basePath = $"/{ModuleName}";
            }

            var content = GetIndexHtmlContent(basePath);
            response.Content = new StringContent(content, Encoding.UTF8, "text/html");
            response.RequestMessage = Request;
            return response;
        }

    }
